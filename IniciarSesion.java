private boolean isRootUser(String codigo, String contra) {
    return codigo.equals("root") && contra.equals("root");
}

private void handleRootLogin() {
    JOptionPane.showMessageDialog(this,"iniciaste sesion como root \n(administrador)");
    openRegistroVentas();
}

private void handleUserLogin(String codigo) {
    String busquedaNombre = metodo.buscarnombre(codigo);
    JOptionPane.showMessageDialog(this, "Bienvenido(a) \n" + busquedaNombre);
    codigoVendedor = codigo;
    openRegistroVentas();
}

private void handleUnregisteredUser() {
    JOptionPane.showMessageDialog(this, "Usuario no registrado, por favor registrese");
}

private void openRegistroVentas() {
    RegistroVentas entrada = new RegistroVentas();
    entrada.setVisible(true);
    this.setVisible(false);
}
    private void btnVerMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerMenuActionPerformed
    
   String codigo = txtCODIGO.getText();
    String contra = txtCONTRA.getText();
    String busquedaUsuario = metodo.buscarUsuarioRegistrado(codigo, contra);

    if (isRootUser(codigo, contra)) {
        handleRootLogin();
    } else if (busquedaUsuario.equals("usuario encontrado")) {
        handleUserLogin(codigo);
    } else {
        handleUnregisteredUser();
    }



       
    }//GEN-LAST:event_btnVerMenuActionPerformed

