    private void btnBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBUSCARActionPerformed
 JOptionPane.showMessageDialog(null, "Por cuestiones de seguridad si desea modificar información del cliente, solo podrá modificar estos campos -Nombre Crédito, Requisitos, Descripción y número de cuenta del cliente-");
    
    conectar cc = new conectar();
    java.sql.Connection cn = cc.conexion();
    ResultSet rs = null;

    try {
        PreparedStatement pst = cn.prepareStatement("SELECT * FROM creditos WHERE codigo = ? AND codigo_aliado = ?");
        pst.setString(1, txtBUSCAR.getText());
        pst.setString(2, IniciarSesion.codigoVendedor);

        rs = pst.executeQuery();

        if (rs.next()) {
            txtCodigoCredito.setText(rs.getString("codigo"));
            txtInteres.setText(rs.getString("interes"));
            cbxNombreCredito.setSelectedItem(rs.getString("nombre"));
            txtNumCuentaCliente.setText(rs.getString("numero_cuenta_cliente"));
            txtCreditoMonto.setText(rs.getString("monto"));
            txtRequisitosCredito1.setText(rs.getString("requisitos"));
            txtDescripcion.setText(rs.getString("descripcion"));

            // Obtener las fechas como cadenas de texto
            String fechaActivacionStr = rs.getString("fecha_activacion");
            String fechaVencimientoStr = rs.getString("fecha_vencimiento");

            // Convertir las cadenas de texto a objetos java.util.Date
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            try {
                java.util.Date fechaActivacion = dateFormat.parse(fechaActivacionStr);
                java.util.Date fechaVencimiento = dateFormat.parse(fechaVencimientoStr);

                // Establecer las fechas en los componentes correspondientes
                DateFechaActivacion.setDate(fechaActivacion);
                DateFechaVencimiento.setDate(fechaVencimiento);
            } catch (ParseException ex) {
                System.err.println(ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No existe una Venta con ese código");
            limpiarCajas();
        }
    } catch (HeadlessException e) {
        System.err.println(e);
    } catch (SQLException ex) {
        Logger.getLogger(RegistroVentas.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        try {
            if (cn != null) {
                cn.close();
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    }//GEN-LAST:event_btnBUSCARActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        
       conectar cc = new conectar();
    Connection cn = cc.conexion();
    try {
        PreparedStatement ps = createUpdateStatement(cn);
        setUpdateStatementParameters(ps);

        int a = ps.executeUpdate();
        if (a > 0) {
            JOptionPane.showMessageDialog(this, "Venta Modificada");
            limpiarCajas();
        } else {
            JOptionPane.showMessageDialog(this, "Error al modificar Venta");
            limpiarCajas();
        }
    } catch (SQLException | HeadlessException e) {
        System.err.println("Error Nota: Todos los datos que no fueron guardados se eliminaran al cerrar el sistema" + e);
    }
    }//GEN-LAST:event_btnActualizarActionPerformed
private PreparedStatement createUpdateStatement(Connection cn) throws SQLException {
    String sql = "UPDATE creditos SET nombre=?, requisitos=?, descripcion=?, numero_cuenta_cliente=? WHERE codigo=? AND codigo_aliado=?";
    return cn.prepareStatement(sql);
}

private void setUpdateStatementParameters(PreparedStatement ps) throws SQLException {
    ps.setString(1, (String) cbxNombreCredito.getSelectedItem());
    ps.setString(2, txtRequisitosCredito1.getText());
    ps.setString(3, txtDescripcion.getText());
    ps.setString(4, txtNumCuentaCliente.getText());
    ps.setString(5, txtCodigoCredito.getText());
    ps.setString(6, IniciarSesion.codigoVendedor);
}
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
           conectar cc = new conectar();
    Connection cn = cc.conexion();
    try {
        if (confirmDelete()) {
            PreparedStatement pst = createDeleteStatement(cn);
            setDeleteStatementParameters(pst);

            int res = pst.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Venta Eliminada");
                limpiarCajas();
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar Venta");
                limpiarCajas();
            }
        }
    } catch (HeadlessException | NumberFormatException | SQLException e) {
        System.err.println(e);
    }
    
    }//GEN-LAST:event_btnEliminarActionPerformed
private boolean confirmDelete() {
    String message = "Si elige si no podra recuperar los datos borrados";
    String title = "¿Esta seguro que dese eliminar la venta?";
    int option = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION);
    return option == JOptionPane.YES_OPTION;
}

private PreparedStatement createDeleteStatement(Connection cn) throws SQLException{
    String sql = "DELETE FROM creditos WHERE codigo=?";
    return cn.prepareStatement(sql);
}

private void setDeleteStatementParameters(PreparedStatement pst) throws SQLException {
    pst.setString(1, txtBUSCAR.getText());
}
