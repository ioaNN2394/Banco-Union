private boolean verificarCampos() {
    JComponent[] campos = {txtCodigoCredito, cbxNombreCredito, txtCreditoMonto, txtRequisitosCredito1, txtDescripcion, txtInteres, txtNumCuentaCliente};
    String[] nombres = {"Código Crédito", "Nombre Crédito", "Monto", "Requisitos", "Descripción", "Interés", "Número de Cuenta Cliente"};

    for (int i = 0; i < campos.length; i++) {
        if (campos[i] instanceof JTextField && ((JTextField) campos[i]).getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo " + nombres[i] + " está vacío");
            return false;
        } else if (campos[i] instanceof JComboBox && ((JComboBox<?>) campos[i]).getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ningún valor en el campo " + nombres[i]);
            return false;
        } else if (campos[i] instanceof JComboBox && ((JComboBox<?>) campos[i]).getSelectedItem().equals("Seleccionar")) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un valor válido en el campo " + nombres[i]);
            return false;
        }
    }

    // Todos los campos están llenos
    return true;
}

private void validarDatos(String codigoCredito, String nombreCredito, BigDecimal monto, String requisitos, String descripcion, BigDecimal interes, String numCuentaCliente) throws Exception {
    
    
    if (!codigoCredito.matches("\\d{1,11}")) {
        throw new Exception("El código de crédito debe ser un número entero de hasta 11 dígitos");
    }
    if (nombreCredito.length() > 45) {
        throw new Exception("El nombre del crédito no puede tener más de 45 caracteres");
    }
    if (monto.compareTo(new BigDecimal("999999999.99")) > 0) {
        throw new Exception("El monto no puede ser mayor a 999999999.99");
    }
    if (requisitos.length() > 100) {
        throw new Exception("Los requisitos no pueden tener más de 100 caracteres");
    }
    if (descripcion.length() > 80) {
        throw new Exception("La descripción no puede tener más de 80 caracteres");
    }
    if (interes.compareTo(new BigDecimal("0.99")) > 0) {
        throw new Exception("El interés no puede ser mayor a 0.99");
    }
     if (!numCuentaCliente.matches("\\d{12}")) {
        throw new Exception("El número de cuenta del cliente debe ser un número entero de 12 dígitos");
    }
}

    
  private boolean agregarVenta(String codigoCredito, String nombreCredito, String fechaActivacion, String fechaVencimiento, BigDecimal monto, String requisitos, String descripcion, BigDecimal interes, String numCuentaCliente, String codigoVendedor) {
    boolean resultado = false;

    conectar cc = new conectar();
    Connection cn = cc.conexion();

    try {
        PreparedStatement ps = cn.prepareStatement("INSERT INTO creditos (codigo, nombre, fecha_activacion, fecha_vencimiento, monto, requisitos, descripcion, interes, numero_cuenta_cliente, codigo_aliado) VALUES (?,?,?,?,?,?,?,?,?,?)");

        ps.setString(1, codigoCredito);
        ps.setString(2, nombreCredito);
        ps.setString(3, fechaActivacion);
        ps.setString(4, fechaVencimiento);
        ps.setBigDecimal(5, monto);
        ps.setString(6, requisitos);
        ps.setString(7, descripcion);
        ps.setBigDecimal(8, interes);
        ps.setString(9, numCuentaCliente);
        ps.setString(10, codigoVendedor);

        int referencia = ps.executeUpdate();

        if (referencia > 0) {
            resultado = true;
        }

    } catch (SQLException e) {
        System.err.println("Error al agregar venta: " + e);
    } finally {
        try {
            if (cn != null) {
                cn.close();
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    
    return resultado;
}




    private void btnVENTAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVENTAActionPerformed
           if (!verificarCampos()) {
        // Hay algún campo vacío o no válido, no se puede continuar
        return;
    }

    String codigoCredito = txtCodigoCredito.getText();
    String nombreCredito = cbxNombreCredito.getSelectedItem().toString();

    BigDecimal monto = new BigDecimal(txtCreditoMonto.getText());
    String requisitos = txtRequisitosCredito1.getText();
    String descripcion = txtDescripcion.getText();
    BigDecimal interes = new BigDecimal(txtInteres.getText());
    String numCuentaCliente = txtNumCuentaCliente.getText();
    String codigoVendedor = IniciarSesion.codigoVendedor;

    // Validar los datos antes de guardarlos en la base de datos
    try {
        validarDatos(codigoCredito, nombreCredito, monto, requisitos, descripcion, interes, numCuentaCliente);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
        return;
    }

    java.util.Date fechaActivacion = DateFechaActivacion.getDate();
    java.util.Date fechaVencimiento = DateFechaVencimiento.getDate();

    // Convertir las fechas a cadenas de texto en el formato deseado
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String fechaActivacionTexto = sdf.format(fechaActivacion);
    String fechaVencimientoTexto = sdf.format(fechaVencimiento);

    boolean resultado = agregarVenta(codigoCredito, nombreCredito, fechaActivacionTexto, fechaVencimientoTexto, monto, requisitos, descripcion, interes, numCuentaCliente, codigoVendedor);

    if (resultado) {
        JOptionPane.showMessageDialog(this, "Venta Agregada");
        limpiarCajas();
    } else {
        JOptionPane.showMessageDialog(this, "Error al agregar Venta");
        limpiarCajas();
    }
    }//GEN-LAST:event_btnVENTAActionPerformed

